def sum(num1:int, num2: int):
    resultado = num1 + num2
    return resultado

def resta(num1:int, num2: int):
    resultado = num1 - num2
    return resultado
    
def multiplica(num1:int, num2: int):
    resultado = num1 * num2
    return resultado

def divide(num1:int, num2: int):
    resultado = num1 / num2
    return resultado